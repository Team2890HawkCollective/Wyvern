package frc.robot.commands;

import edu.wpi.first.wpilibj2.command.CommandBase;
import frc.robot.subsystems.SensorSubsystem;

public class SensorCommand extends CommandBase {
  private final SensorSubsystem m_sensorSubsystem;
  
  /**
   * Creates a new SensorCommand.
   */
  public SensorCommand(SensorSubsystem subsystem) {
    // Use addRequirements() here to declare subsystem dependencies.
    m_sensorSubsystem = subsystem;

    addRequirements(subsystem);
  }

  // Called when the command is initially scheduled.
  @Override
  public void initialize() {
  }

  // Called every time the scheduler runs while the command is scheduled.
  @Override
  public void execute() {
    m_sensorSubsystem.balancePidgeon();
  }

  // Called once the command ends or is interrupted.
  @Override
  public void end(boolean interrupted) {
  }

  // Returns true when the command should end.
  @Override
  public boolean isFinished() {
    return false;
  }
}
